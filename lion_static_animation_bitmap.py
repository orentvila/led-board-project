#!/usr/bin/env python3
"""
Lion Bitmap Static Image for LED Board
Displays a static lion bitmap image for 5 seconds
"""

import time
from led_controller_exact import LEDControllerExact
import config

class LionStaticAnimationBitmap:
    def __init__(self):
        """Initialize the lion bitmap static animation."""
        self.led = LEDControllerExact()
        self.width = config.TOTAL_WIDTH  # 32
        self.height = config.TOTAL_HEIGHT  # 48
        
        # Lion bitmap data (32x48 pixels)
        bitmap_hex = [
            0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
            0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
            0xff, 0xff, 0xff, 0xff, 0xff, 0xf8, 0x3f, 0xff, 0xff, 0xff, 0xf7, 0xff, 0xff, 0xbf, 0xfb, 0xff,
            0xff, 0xbf, 0xfb, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0x7e, 0xdf, 0xff, 0xff, 0x6f, 0xff, 0xdf,
            0xff, 0x6f, 0xfd, 0xdf, 0xff, 0x77, 0xff, 0xef, 0xff, 0x74, 0x7f, 0x6f, 0xff, 0x76, 0xff, 0x9f,
            0xff, 0x76, 0xff, 0x9f, 0xff, 0x39, 0x3b, 0xbf, 0xff, 0xdc, 0x77, 0x7f, 0xff, 0xdf, 0xef, 0x7f,
            0xff, 0xff, 0xda, 0xbf, 0xff, 0xbf, 0xf9, 0x7f, 0xfe, 0xbe, 0xf8, 0xff, 0xff, 0xbf, 0xfa, 0xff,
            0xff, 0xff, 0xfa, 0xff, 0xff, 0xff, 0xfe, 0xff, 0xff, 0xff, 0xf6, 0xff, 0xfe, 0xdb, 0xa7, 0xff,
            0xfd, 0xdf, 0x77, 0xff, 0xfc, 0x81, 0x00, 0x7f, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
            0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
            0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
            0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff
        ]
        
        # Convert bitmap to 32x48 pixel array
        self.lion_pixels = []
        for row in range(48):
            row_data = []
            byte_start = row * 4
            for col in range(32):
                byte_index = byte_start + (col // 8)
                bit_index = 7 - (col % 8)
                byte_value = bitmap_hex[byte_index]
                pixel = (byte_value >> bit_index) & 1
                # Invert: 0 = lion pixel, 1 = background
                row_data.append(1 - pixel)
            self.lion_pixels.append(row_data)
        
        self.lion_color = (255, 255, 255)  # White for lion
        
    def safe_set_pixel(self, x, y, color):
        """Safely set a pixel if coordinates are within bounds."""
        if 0 <= x < self.width and 0 <= y < self.height:
            self.led.set_pixel(x, y, color)
    
    def draw_lion(self):
        """Draw the lion from the bitmap data."""
        self.led.clear()
        
        for y in range(min(self.height, 48)):
            for x in range(min(self.width, 32)):
                if self.lion_pixels[y][x] == 1:
                    self.safe_set_pixel(x, y, self.lion_color)
        
        self.led.show()
    
    def run_animation(self, should_stop=None):
        """Run the lion bitmap static image."""
        duration = 5
        start_time = time.time()
        
        print("ðŸ¦ Starting lion bitmap static image...")
        
        self.draw_lion()
        
        while time.time() - start_time < duration:
            if should_stop and should_stop():
                print("ðŸ¦ Lion bitmap image stopped by user")
                break
            time.sleep(0.1)
        
        print("ðŸ¦ Lion bitmap image completed!")
        self.led.clear()
        self.led.show()
    
    def cleanup(self):
        """Clean up resources."""
        self.led.cleanup()

