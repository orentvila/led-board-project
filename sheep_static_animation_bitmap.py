#!/usr/bin/env python3
"""
Sheep Bitmap Static Image for LED Board
Displays a static sheep bitmap image for 5 seconds
"""

import time
from led_controller_exact import LEDControllerExact
import config

class SheepStaticAnimationBitmap:
    def __init__(self):
        """Initialize the sheep bitmap static animation."""
        self.led = LEDControllerExact()
        self.width = config.TOTAL_WIDTH
        self.height = config.TOTAL_HEIGHT
        
        # Sheep bitmap data (32x48 pixels)
        bitmap_hex = [
            0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
            0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
            0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
            0xff, 0xff, 0xff, 0xff, 0xff, 0xf0, 0x7f, 0xff, 0xff, 0xc7, 0x1f, 0xff, 0xff, 0x87, 0x87, 0xff,
            0xff, 0x3f, 0xe7, 0xff, 0xff, 0x37, 0x63, 0xff, 0xfc, 0x00, 0x01, 0xff, 0xf8, 0x88, 0x88, 0xff,
            0xfc, 0x1b, 0x41, 0xff, 0xfe, 0x1f, 0xc1, 0xff, 0xfc, 0xcf, 0x99, 0xff, 0xfc, 0xe7, 0x9d, 0xff,
            0xfc, 0xe7, 0x3d, 0xff, 0xf9, 0xf0, 0x7c, 0xff, 0xf9, 0xff, 0xfc, 0xff, 0xfc, 0xff, 0xfc, 0xff,
            0xfc, 0xff, 0xfd, 0xff, 0xfc, 0xff, 0xfd, 0xff, 0xfe, 0xff, 0xf9, 0xff, 0xfe, 0x3f, 0xe3, 0xff,
            0xff, 0x3f, 0xe7, 0xff, 0xff, 0x07, 0x07, 0xff, 0xff, 0x00, 0x07, 0xff, 0xff, 0x07, 0x07, 0xff,
            0xff, 0x07, 0x87, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
            0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
            0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff
        ]
        
        self.sheep_pixels = []
        for row in range(48):
            row_data = []
            byte_start = row * 4
            for col in range(32):
                byte_index = byte_start + (col // 8)
                bit_index = 7 - (col % 8)
                byte_value = bitmap_hex[byte_index]
                pixel = (byte_value >> bit_index) & 1
                row_data.append(1 - pixel)
            self.sheep_pixels.append(row_data)
        
        self.sheep_color = (255, 255, 255)
        
    def safe_set_pixel(self, x, y, color):
        if 0 <= x < self.width and 0 <= y < self.height:
            self.led.set_pixel(x, y, color)
    
    def draw_sheep(self):
        self.led.clear()
        for y in range(min(self.height, 48)):
            for x in range(min(self.width, 32)):
                if self.sheep_pixels[y][x] == 1:
                    self.safe_set_pixel(x, y, self.sheep_color)
        self.led.show()
    
    def run_animation(self, should_stop=None):
        duration = 5
        start_time = time.time()
        print("ðŸ‘ Starting sheep bitmap static image...")
        self.draw_sheep()
        while time.time() - start_time < duration:
            if should_stop and should_stop():
                print("ðŸ‘ Sheep bitmap image stopped by user")
                break
            time.sleep(0.1)
        print("ðŸ‘ Sheep bitmap image completed!")
        self.led.clear()
        self.led.show()
    
    def cleanup(self):
        self.led.cleanup()

