#!/usr/bin/env python3
"""
Snail Bitmap Static Image for LED Board
Displays a static snail bitmap image for 5 seconds
"""

import time
from led_controller_exact import LEDControllerExact
import config

class SnailStaticAnimationBitmap:
    def __init__(self):
        """Initialize the snail bitmap static animation."""
        self.led = LEDControllerExact()
        self.width = config.TOTAL_WIDTH
        self.height = config.TOTAL_HEIGHT
        
        # Snail bitmap data (32x48 pixels)
        bitmap_hex = [
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x02, 0x00, 0x00, 0x00, 0x03, 0x20, 0x00, 0x00, 0x01, 0x20, 0x00, 0x7f, 0x01, 0x20,
            0x00, 0xe1, 0xc1, 0xe0, 0x01, 0x80, 0x61, 0xc0, 0x03, 0x00, 0x23, 0xe0, 0x06, 0x38, 0x36, 0x20,
            0x04, 0x7c, 0x1e, 0x20, 0x04, 0x46, 0x1c, 0x60, 0x04, 0x42, 0x18, 0x40, 0x04, 0x02, 0x18, 0xc0,
            0x04, 0x07, 0xf0, 0xc0, 0x07, 0xff, 0xe1, 0x80, 0x03, 0x00, 0x01, 0x00, 0x06, 0x00, 0x03, 0x00,
            0x0c, 0x00, 0x06, 0x00, 0x0f, 0xff, 0xfc, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00
        ]
        
        self.snail_pixels = []
        for row in range(48):
            row_data = []
            byte_start = row * 4
            for col in range(32):
                byte_index = byte_start + (col // 8)
                bit_index = 7 - (col % 8)
                byte_value = bitmap_hex[byte_index]
                pixel = (byte_value >> bit_index) & 1
                # NO INVERSION: 0 = background, 1 = snail pixel
                row_data.append(pixel)
            self.snail_pixels.append(row_data)
        
        self.snail_color = (255, 255, 255)
        
    def safe_set_pixel(self, x, y, color):
        if 0 <= x < self.width and 0 <= y < self.height:
            self.led.set_pixel(x, y, color)
    
    def draw_snail(self):
        self.led.clear()
        for y in range(min(self.height, 48)):
            for x in range(min(self.width, 32)):
                if self.snail_pixels[y][x] == 1:
                    self.safe_set_pixel(x, y, self.snail_color)
        self.led.show()
    
    def run_animation(self, should_stop=None):
        duration = 5
        start_time = time.time()
        print("ðŸŒ Starting snail bitmap static image...")
        self.draw_snail()
        while time.time() - start_time < duration:
            if should_stop and should_stop():
                print("ðŸŒ Snail bitmap image stopped by user")
                break
            time.sleep(0.1)
        print("ðŸŒ Snail bitmap image completed!")
        self.led.clear()
        self.led.show()
    
    def cleanup(self):
        self.led.cleanup()

